def _regression_evidence(root: Path) -> RegressionEvidence:
    signature = "assertion:expected-1-got-0"
    _seed_execution_loop(root)
    _write(
        root,
        "tests/regression_probe.py",
        "from pathlib import Path\n"
        "source = Path('src/bug.py').read_text(encoding='utf-8')\n"
        f"print({signature!r}) if 'return 1' not in source else print('passed')\n"
        "raise SystemExit(0 if 'return 1' in source else 1)\n",
    )
    green_source = (root / "src" / "bug.py").read_text("utf-8")
    _write(root, "src/bug.py", "def _value():\n    return 0\n")
    options = {
        "root": root,
        "loop_id": "impl-test",
        "test_id": "test-value",
        "test_symbol": "tests.regression_probe",
        "command_argv": (sys.executable, "tests/regression_probe.py"),
        "test_source_ref": "tests/regression_probe.py",
        "failure_signature": signature,
    }
    red = capture_regression_phase(LeanRegressionOptions(phase="red", **options))
    assert red.status == "ready", red
    _write(root, "src/bug.py", green_source)
    green = capture_regression_phase(LeanRegressionOptions(phase="green", **options))
    assert green.status == "ready", green
    return RegressionEvidence.model_validate_json(
        (root / green.evidence_path).read_text("utf-8")
    )


